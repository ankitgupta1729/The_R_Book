# Generating Sequences
print(0:10)
print(15:5)
print(seq(2,5,0.1))
print(seq(5,2,-0.1))
N <- c(55,76,92,103,84,88,121,91,65,77,99)
print(seq(from=0.04,by=0.01,length=11))
print(seq(0.04,by=0.01,along=N))
print(seq(from=0.04,to=0.14,along=N))
print(seq(1.4,2.1,0.3))
print(sequence(c(4,3,4,4,4,5)))
# Generating repeats
print(rep(9,5))
print(rep(1:4, 2))
print(rep(1:4, each = 2))
print(rep(1:4, each = 2, times = 3))
print(rep(1:4,1:4))
print(rep(1:4,c(4,1,4,2)))
print(rep(c("cat","dog","gerbil","goldfish","rat"),c(2,3,2,1,3)))
# Generating factor levels
print(gl(4,3))
print(gl(4,3,24))
Temp <- gl(2, 2, 24, labels = c("Low", "High"))
Soft <- gl(3, 8, 24, labels = c("Hard","Medium","Soft"))
M.user <- gl(2, 4, 24, labels = c("N", "Y"))
Brand <- gl(2, 1, 24, labels = c("X", "M"))
print(data.frame(Temp,Soft,M.user,Brand))